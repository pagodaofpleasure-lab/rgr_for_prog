DES::DES(uint64_t key) { generateSubkeys(key); }

uint64_t DES::permute(uint64_t val, const uint8_t* table, int n) {
    uint64_t res = 0;
    for (int i = 0; i < n; ++i) {
        if ((val >> (64 - table[i])) & 1) res |= (1ULL << (n - 1 - i));
    }
    return res;
}

void DES::generateSubkeys(uint64_t key) {
    uint64_t pc1 = permute(key, PC1, 56);
    uint32_t C = (uint32_t)((pc1 >> 28) & 0x0FFFFFFF);
    uint32_t D = (uint32_t)(pc1 & 0x0FFFFFFF);
    for (int i = 0; i < 16; ++i) {
        for (int j = 0; j < shifts[i]; ++j) {
            C = ((C << 1) & 0x0FFFFFFF) | (C >> 27);
            D = ((D << 1) & 0x0FFFFFFF) | (D >> 27);
        }
        subkeys[i] = permute(((uint64_t)C << 28) | D, PC2, 48);
    }
}

uint32_t DES::f(uint32_t R, uint64_t k) {
    uint64_t e = permute((uint64_t)R << 32, E, 48) ^ k;
    uint32_t res = 0;
    for (int i = 0; i < 8; ++i) {
        uint8_t val = (e >> (42 - i * 6)) & 0x3F;
        uint8_t row = ((val >> 4) & 2) | (val & 1);
        uint8_t col = (val >> 1) & 0x0F;
        res = (res << 4) | S[i][row * 16 + col];
    }
    return (uint32_t)permute((uint64_t)res << 32, P, 32);
}

uint64_t DES::encrypt(uint64_t block) {
    block = permute(block, IP, 64);
    uint32_t L = (uint32_t)(block >> 32), R = (uint32_t)block;
    for (int i = 0; i < 16; ++i) {
        uint32_t tmp = R;
        R = L ^ f(R, subkeys[i]);
        L = tmp;
    }
    return permute(((uint64_t)R << 32) | L, FP, 64);
}

uint64_t DES::decrypt(uint64_t block) {
    block = permute(block, IP, 64);
    uint32_t L = (uint32_t)(block >> 32), R = (uint32_t)block;
    for (int i = 15; i >= 0; --i) {
        uint32_t tmp = R;
        R = L ^ f(R, subkeys[i]);
        L = tmp;
    }
    return permute(((uint64_t)R << 32) | L, FP, 64);
}
